    private void fillTable() {
        List<UserProfile> userProfiles = new ArrayList<>();
        try {
            userProfiles = Controller.getInstance().getAllUsers();
            UserProfileTableModel uptm = new UserProfileTableModel(userProfiles);
            tblUserStatus.setModel(uptm);

        } catch (Exception ex) {
            Logger.getLogger(UsersStatusForm.class
                    .getName()).log(Level.SEVERE, null, ex);
        }

    }
