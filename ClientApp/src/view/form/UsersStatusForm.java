    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selectedRow = tblUserStatus.getSelectedRow();
        if (tblUserStatus.getSelectedRowCount() != 1) {
            JOptionPane.showMessageDialog(this, "Morate izabrati tacno jednog korisnika", "Upozorenje", JOptionPane.INFORMATION_MESSAGE);
        } else {
            UserProfileTableModel uptm = (UserProfileTableModel) tblUserStatus.getModel();
            UserProfile user = uptm.getUser(selectedRow);
            new EditUsersStatusForm(null, true, user, tblUserStatus).setVisible(true);
        }
    }//GEN-LAST:event_btnEditActionPerformed

