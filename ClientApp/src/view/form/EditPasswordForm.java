    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            if (validation()) {
                int answer=JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da promenite lozinku?", "Upozorenje", JOptionPane.YES_NO_OPTION);
                if(answer==JOptionPane.YES_OPTION){
                    try {
                        user.setPassword(new String(pswNewPsw.getPassword()));
                        Controller.getInstance().editUserPassword(user);
                         dispose();
                         JOptionPane.showMessageDialog(this, "Lozinka je uspesno promenjena.", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(this, "Greska pri cuvanju izmena nad lozinkom u bazi", "Upozorenje", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Upozorenje", JOptionPane.ERROR_MESSAGE);
        }
        


    }//GEN-LAST:event_btnSaveActionPerformed


