    private void prepareLabels() {
        pswConfirmPsw.setText(null);
        pswCurrentPsw.setText(null);
        pswNewPsw.setText(null);
        lblUsername.setText(user.getUsername());
    }

    private boolean validation() throws Exception {
        if (new String(pswNewPsw.getPassword()).isEmpty() || new String(pswConfirmPsw.getPassword()).isEmpty() || new String(pswCurrentPsw.getPassword()).isEmpty()) {
            throw new Exception("Nevalidan unos, odredjena polja su prazna.");
        }
        if (!user.getPassword().equals(new String(pswCurrentPsw.getPassword()))) {
            throw new Exception("Greska, trenutna lozinka nije tacna.");
        }
        if (new String(pswNewPsw.getPassword()).equalsIgnoreCase(user.getUsername()) || new String(pswNewPsw.getPassword()).equalsIgnoreCase(user.getEmployer().getFirstname()) || new String(pswNewPsw.getPassword()).equalsIgnoreCase(user.getEmployer().getLastname())) {
            throw new Exception("Lozinka je slaba, ne mozete koristiti ime, prezime ili username kao lozinku.");
        }
        if (!new String(pswNewPsw.getPassword()).equals(new String(pswConfirmPsw.getPassword()))) {
            throw new Exception("Greska, lozinke se ne poklapaju.");
        }return true;
    }
