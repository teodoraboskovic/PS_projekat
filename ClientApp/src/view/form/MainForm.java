    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        int answer = JOptionPane.showConfirmDialog(this, "Sistem ce vas automatski odjaviti. Da li ste sigurni da zelite da zavrsite sa radom?", "Upozorenje", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (answer == JOptionPane.YES_OPTION) {
            try {
                user = Controller.getInstance().logout(user);
                JOptionPane.showMessageDialog(this, "Korisnik " + user.getEmployer().toString() + " je uspesno izlogovan", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
                dispose();
                System.exit(0);
            } catch (Exception ex) {
                dispose();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Radnja otkazana, nastavite sa radom", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_formWindowClosing

