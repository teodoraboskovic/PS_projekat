    private void jmiLogoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiLogoutActionPerformed
        int answer = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da se odjavite sa sistema?", "Upozorenje", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (answer == JOptionPane.YES_OPTION) {
            try {
                user = Controller.getInstance().logout(user);
                JOptionPane.showMessageDialog(this, "Korisnik " + user.getEmployer().toString() + " je uspesno izlogovan", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
                dispose();
                System.exit(0);
            } catch (Exception ex) {
                dispose();
            }
        }
    }//GEN-LAST:event_jmiLogoutActionPerformed

