    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int answer = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da sacuvate sve izmene u bazi? ", "Upozorenje", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (answer == JOptionPane.YES_OPTION) {
            ParentsTableModel ptm = (ParentsTableModel) tblParents.getModel();
            List<Parent> editedParents = ptm.getEditedParents();
            List<Parent> deletedParents = ptm.getDeletedParents();
            try {
                EditAndDeleteList editAndDeleteList = new EditAndDeleteList(editedParents, deletedParents);
                Controller.getInstance().editAndDeleteParents(editAndDeleteList);
                JOptionPane.showMessageDialog(this, "Sistem je zapamtio i obrisao roditelje/staratelje.", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti ili obrise roditelje/staratelje.", "Upozorenje", JOptionPane.ERROR_MESSAGE);
                fillTable();
            }

        }
    }//GEN-LAST:event_btnSaveActionPerformed

