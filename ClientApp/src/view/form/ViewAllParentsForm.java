    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        txtJMBG.setEnabled(false);
        btnSearch.setEnabled(false);
        cbViewAll.setEnabled(false);
        Parent parent;
        int selectedRow = tblParents.getSelectedRow();
        if (tblParents.getSelectedRowCount() != 1) {
            JOptionPane.showMessageDialog(this, "Morate selektovati tacno jednog roditelja/staretelja", "Upozorenje", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                ParentsTableModel ptm = (ParentsTableModel) tblParents.getModel();
                parent = ptm.getParent(selectedRow);
                parent = Controller.getInstance().getParent(parent);
                if (parent != null) {
                    JOptionPane.showMessageDialog(this, "Sistem je ucitao roditelja/staretelja.", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
                    new EditParentForm(null, true, parent, tblParents, Mode.EDIT).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita roditelja/staretelja.", "Upozorenje", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita roditelja/staratelja.", "Upozorenje", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

