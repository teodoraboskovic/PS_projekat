    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int answer = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da izmenite status korisnika " + lblFirstname.getText() + " " + lblLastname.getText() + "?", "Upozorenje", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (answer == JOptionPane.YES_OPTION) {
            try {
                if (validation(cbStatus.getSelectedItem())) {
                    UserProfileTableModel uptm=(UserProfileTableModel) tblUserProfile.getModel();
                    uptm.editTable(user.getUserId(), cbStatus.getSelectedItem().toString());
                    dispose();
                    
                } 
            } catch (Exception ex) {
                 JOptionPane.showMessageDialog(this, ex.getMessage(), "Upozorenje", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

