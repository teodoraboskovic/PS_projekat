    private void prepareLabels() {
        lblFirstname.setText(user.getEmployer().getFirstname());
        lblLastname.setText(user.getEmployer().getLastname());
    }

    private boolean validation(Object selectedItem) throws Exception {
        if (selectedItem == null) {
            throw new Exception("Nevalidan unos, odredjena polja su prazna.");
        }
        return true;
    }

    private void fillComboBox() {
        cbStatus.removeAllItems();
        cbStatus.addItem("ACTIVE");
        cbStatus.addItem("NOT_ACTIVE");
        cbStatus.addItem("DISABLED");
        if (user.getUserStatus().equals(UserStatus.valueOf("ACTIVE"))) {
            cbStatus.setSelectedIndex(0);
        } else if (user.getUserStatus().equals(UserStatus.valueOf("NOT_ACTIVE"))) {
            cbStatus.setSelectedIndex(1);
        } else if (user.getUserStatus().equals(UserStatus.valueOf("DISABLED"))) {
            cbStatus.setSelectedIndex(2);
        }
    }

