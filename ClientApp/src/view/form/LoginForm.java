    private boolean validation(String username, char[] password) throws Exception {
        if (username.isEmpty() || password==null) {
            throw new Exception("Nevalidan unos, odredjena polja su prazna.");
        }
        if(username.length()>30 || password.length>30){
             throw new Exception("Nevalidan unos, maksimalan broj karaktera za korisnicko ime i lozinku je 30.");
           
        }
        return true;
    }

   
