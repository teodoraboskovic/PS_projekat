    private void brnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnLoginActionPerformed
        try {
            if (validation(txtUsername.getText(), txtPassword.getPassword())) {
                try {
                    String password = new String(txtPassword.getPassword());
                    String username = txtUsername.getText();
                    UserProfile user = new UserProfile();
                    user.setPassword(password);
                    user.setUsername(username);
                    user = Controller.getInstance().login(user);
                    JOptionPane.showMessageDialog(this, "Korisnik " + user.getEmployer().toString() + " je uspesno ulogovan", "Login", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                    new MainForm(user).setVisible(true);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Greska pri logovanju! Pokusajte ponovo", "Login", JOptionPane.ERROR_MESSAGE);
                }
            } 
        } catch (Exception ex) {            
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Upozorenje", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_brnLoginActionPerformed

